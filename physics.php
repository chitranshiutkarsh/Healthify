            <p>Physical therapy (PT), also known as physiotherapy, by using mechanical force and movements (bio-mechanics or kinesiology), manual therapy, exercise therapy, and electrotherapy, remediates impairments and promotes mobility and function. Physical therapy is used to improve a patient's quality of life through 
                examination, diagnosis, prognosis, physical intervention, and patient education. It is performed
                 by physical therapists (known as physiotherapists in many countries).</p>
            <p>
                In addition to clinical practice, other activities encompassed in the physical therapy profession include research, education, consultation and administration. 
                Physical therapy services may be provided as primary care treatment or alongside, or in conjunction with, other medical services.
