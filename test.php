        <p>A medical laboratory or clinical laboratory is a laboratory where clinical pathology tests are carried out on clinical specimens to obtain information about the health of a patient to aid in diagnosis, treatment, and prevention of disease.
            Clinical Medical laboratories are an example of applied science, as opposed to research laboratories that focus on basic science, 
            such as found in some academic institutions.</p>
        <p>
                Medical laboratories vary in size and complexity and so offer a variety of testing services. More comprehensive services can be found in acute-care hospitals and medical centers, where 70% of clinical decisions are based on laboratory testing. 
                Doctors offices and clinics, as well as skilled nursing and long-term care facilities, may have laboratories that provide more basic 
                testing services. Commercial medical laboratories operate as independent businesses and provide testing that is otherwise not provided 
                in other settings due to low test volume or complexity.
