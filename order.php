        <p>Conventional stationary pharmacies usually have controlled drug distribution systems from the manufacturer. Validation and good distribution practices are followed. 
            Home delivery of pharmaceuticals can be a desirable convenience but sometimes there can be problems with uncontrolled distribution.</p>
        <p>The shipment of drugs through the mail and parcel post is sometimes a concern for temperature-sensitive pharmaceuticals. Uncontrolled shipping conditions can include high and low temperatures outside of the listed storage conditions for a drug. 
            For example, the US FDA found the temperature in a mail box in the sun could 
            reach 136 째F (58 째C) while the ambient air temperature was 101 째F (38 째C)</p>  
        <p>
            Shipment by express mail and couriers reduces transit time and often involves delivery to the door, rather than a mail box. 
            The use of insulated shipping containers also helps control drug temperatures, reducing risks to drug safety and efficacy.
